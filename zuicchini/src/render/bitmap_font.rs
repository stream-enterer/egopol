    /// Render a glyph bitmap into an `Image` at the given position with the given color.
    /// Test helper: stamps set bits with `color`, leaves unset bits unchanged.
    fn render_glyph_to_image(
        glyph: &[u8],
        glyph_w: u32,
        glyph_h: u32,
        target: &mut Image,
        tx: u32,
        ty: u32,
        color: Color,
    ) {
        let tw = target.width();
        let th = target.height();
        let ch = target.channel_count();
        for row in 0..glyph_h {
            let py = ty + row;
            if py >= th {
                break;
            }
            let byte = glyph[row as usize];
            if byte == 0 {
                continue;
            }
            for col in 0..glyph_w {
                let px = tx + col;
                if px >= tw {
                    break;
                }
                if byte & (0x80 >> col) != 0 {
                    let p = target.pixel_mut(px, py);
                    if ch == 1 {
                        p[0] = color.a();
                    } else {
                        p[0] = color.r();
                        p[1] = color.g();
                        p[2] = color.b();
                        if ch >= 4 {
                            p[3] = color.a();
                        }
                    }
                }
            }
        }
    }

