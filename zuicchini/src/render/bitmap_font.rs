/// Get glyph bitmap for a character. Returns `(glyph_data_slice, width, height)`.
/// Returns the '?' glyph for characters outside the printable ASCII range.
pub(crate) fn get_glyph(ch: char) -> (&'static [u8], u32, u32) {
    let cp = ch as u32;
    let index = if (FIRST_CHAR..=LAST_CHAR).contains(&cp) {
        (cp - FIRST_CHAR) as usize
    } else {
        // Fall back to '?'
        (b'?' - FIRST_CHAR as u8) as usize
    };
    let start = index * CHAR_HEIGHT as usize;
    let end = start + CHAR_HEIGHT as usize;
    (&GLYPH_DATA[start..end], CHAR_WIDTH, CHAR_HEIGHT)
}

/// Measure text width in characters, handling formatted text (tabs and newlines).
/// Returns `(max_columns, row_count)`.
pub(crate) fn measure_formatted(text: &str) -> (usize, usize) {
    let mut max_cols = 0usize;
    let mut rows = 1usize;
    let mut col = 0usize;

    let mut chars = text.chars().peekable();
    while let Some(ch) = chars.next() {
        match ch {
            '\r' => {
                // \r\n or lone \r both count as one newline
                if chars.peek() == Some(&'\n') {
                    chars.next();
                }
                max_cols = max_cols.max(col);
                col = 0;
                rows += 1;
            }
            '\n' => {
                max_cols = max_cols.max(col);
                col = 0;
                rows += 1;
            }
            '\t' => {
                // Align to next multiple of 8
                col = (col / 8 + 1) * 8;
            }
            _ => {
                col += 1;
            }
        }
    }
    max_cols = max_cols.max(col);
    (max_cols, rows)
}

