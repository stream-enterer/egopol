    fn space_is_blank() {
        let (glyph, w, h) = get_glyph(' ');
        assert_eq!(w, 8);
        assert_eq!(h, 14);
        assert!(glyph.iter().all(|&b| b == 0));
    }

    #[test]
    fn fallback_to_question_mark() {
        let (g1, _, _) = get_glyph('?');
        let (g2, _, _) = get_glyph('\u{FFFF}');
        assert_eq!(g1, g2);
    }

    #[test]
    fn render_glyph_writes_pixels() {
        let mut img = Image::new(8, 14, 4);
        let (glyph, w, h) = get_glyph('A');
        render_glyph_to_image(glyph, w, h, &mut img, 0, 0, Color::WHITE);
        // 'A' should have some non-zero pixels
        let has_pixels = (0..14)
            .flat_map(|y| (0..8).map(move |x| (x, y)))
            .any(|(x, y)| img.pixel(x, y)[0] > 0);
        assert!(has_pixels, "'A' glyph should render visible pixels");
    }

